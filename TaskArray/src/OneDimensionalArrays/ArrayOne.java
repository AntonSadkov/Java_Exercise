package OneDimensionalArrays;

public class ArrayOne {
    public static void main(String[] args) {
        int[] nums = new int[4];
// устанавливаем значения элементов массива
        nums[0] = 1;
        nums[1] = 2;
        nums[2] = 4;
        nums[3] = 100;

// получаем значение третьего элемента массива
        System.out.println(nums[3]);    // 4
    }
}