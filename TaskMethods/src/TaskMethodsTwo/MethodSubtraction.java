package TaskMethodsTwo;

public class MethodSubtraction {
    static void Subtraction(double a, double b){
        double z = a - b;
        System.out.printf("Результат вычитания: %.1f", z);
    }
    static void Subtraction(double a, double b, double c){
        double z = a - b - c;
        System.out.printf("Результат вычитания: %.1f", z);
    }
    static void Subtraction(double a, double b, double c, double d){
        double z = a - b - c - d;
        System.out.printf("Результат вычитания: %.1f", z);
    }
    static void Subtraction(double a, double b, double c, double d, double e){
        double z = a - b - c - d - e;
        System.out.printf("Результат вычитания: %.1f", z);
    }
}